package Variables;

public class First {

    public static void main(String args[]) {

        int marks = 80;
        int age = 20;
        char aa = 'a';
        char bb = 'A';
        float f = 3.14f;
        String name = "vinod";

        System.out.println("marks: " + marks);
        System.out.println("age: " + age);
        System.out.println("char a: " + aa);
        System.out.println("char A: " + bb);
        System.out.println("float: " + f);
        System.out.println("Name: " + name);
    }

}
