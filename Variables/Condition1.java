package Variables;

public class Condition1 {

    public static void main(String args[]) {
        int a = 20, b = 10;
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);
        System.out.println(a != b);

    }

}
