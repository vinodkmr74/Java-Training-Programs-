package Variables;

public class Condition2 {

    public static void main(String[] args) {
        int a = 100, b = 90;

        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a != b);
        System.out.println(a == b);
        System.out.println(a >= b);
    }

}
