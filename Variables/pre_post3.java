package Variables;

public class pre_post3 {

    public static void main(String[] args) {

        int a = 5;
        System.out.println(a--);
        System.out.println(--a);
        System.out.println(--a);
        System.out.println(a++);
        System.out.println(a--);
        System.out.println(++a);
        System.out.println(a++);
        System.out.println(a);
    }

}
