package Variables;

public class Pre_post {

    public static void main(String[] args) {
        int a = 100;
        System.out.println(a++);
        System.out.println(a++);
        System.out.println(++a);
        System.out.println(a--);
        System.out.println(--a);
        System.out.println(a++);
        System.out.println(a);
        System.out.println(++a);
        System.out.println(a--);
        System.out.println(a++);

    }

}
