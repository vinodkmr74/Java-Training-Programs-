package Variables;

public class Condition {

    public static void main(String[] args) {
        int a = 150, b = 139;
        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);
    }

}
