package For_loop;

public class Nx {
    public static void main(String[] args) {
        int n = 5;
        for (int x = 1; x <= 5; x++) {
            // for(int x=1;x<=n;x++)
            System.out.print(n);
            System.out.print("#");
            System.out.print(x);
            n--;
        }
    }

}
